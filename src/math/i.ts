/**
 * mix 函数将两个数字相减并返回结果。
 *
 * :param a: a 是一个数字参数，表示计算中的第一个数字。
 * :type a: number
 * :param b: 参数“b”是函数中从参数“a”中减去的数字。
 * :type b: number
 * :return: 两个输入数字 a 和 b 之间的差。
 */
function mix (a: number, b: number): number {
  return a - b;
}
